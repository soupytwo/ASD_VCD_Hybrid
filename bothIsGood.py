def descSaver(description, filename):
    with open("results.txt", "a") as file:
        file.write(f"Image: {filename}\n")

        file.write(f"Description: {description}\n")

        file.write("=" * 50 + "\n")

class ASD_VCDProcessor(LogitsProcessor):
    def __init__(self, adjusted_logits):
        super().__init__()
        self.adjusted = adjusted_logits
        self.count = 0

    def __call__(self, input_ids, scores):
        if self.count < self.adjusted.size(0):
            scores = self.adjusted[self.count]
        self.count += 1
        return scores

def gaussInator(image):
    og = np.array(image, dtype=np.float32) / 255.0

    mean = 0
    var = 0.10
    sigma = np.sqrt(var)
    noise = np.random.normal(loc=mean, scale=sigma, size=og.shape)

    corrupted = np.clip(og + noise, 0.0, 1.0)
    corrupted_pil = Image.fromarray((corrupted * 255).astype(np.uint8))
    return corrupted_pil

def ASD_VCD_Inator(image, steering_vector: torch.Tensor, filename, max_new_tokens=64):
    alpha = 0.1
    layer = 10

    clean = Image.open(image)
    noisy = gaussInator(clean)

    conversation = [
        {
            "role": "user",
            "content": [
                {"type": "image", "image": image},
                {"type": "text", "text": "What is shown in this image? Describe it in detail."},
            ],
        },
    ]

    inputsClean = processor.apply_chat_template(conversation, add_generation_prompt=True, tokenize=True, return_dict=True, return_tensors="pt")
    
    #prevent runtime error
    inputsClean = {key: value.to(model.device) for key, value in inputsClean.items()}
    
    def make_hook(direction):
        def hook(module, input, output):
            #prevent weird type errors
            if isinstance(output, tuple):
                hidden_states, *rest = output
                newVector = hidden_states + direction * steering_vector.to(hidden_states.device)
                return (newVector, *rest)
            return output + direction * steering_vector.to(output.device)
        return hook
    
    hook_f = make_hook(+1)
    # many models because it's just a way to access the layers
    hook_pos = model.model.language_model.layers[layer].register_forward_hook(hook_f)

    out_pos = model.generate(**inputsClean, max_new_tokens=max_new_tokens, output_hidden_states=True, output_scores=True, return_dict_in_generate=True)
    hook_pos.remove()

    print("Positive pass completed.")

    hook_b = make_hook(-1)
    hook_neg = model.model.language_model.layers[layer].register_forward_hook(hook_b)
    
    out_neg = model.generate(**inputsClean, max_new_tokens=max_new_tokens, output_hidden_states=True,  output_scores=True, return_dict_in_generate=True)
    hook_neg.remove()

    print("Negative pass completed.")

    
    logits_pos = torch.stack(out_pos.scores, dim=1)
    logits_neg = torch.stack(out_neg.scores, dim=1)
    clean_logits_asd = (1 + alpha) * logits_pos - alpha * logits_neg

    conversation[0]["content"][0]["image"] = noisy
    inputsNoisy = processor.apply_chat_template(conversation, add_generation_prompt=True, tokenize=True, return_dict=True, return_tensors="pt").to(model.device, torch.float16)
    inputsNoisy = {key: value.to(model.device) for key, value in inputsNoisy.items()}

    out_noisy = model.generate(
        **inputsNoisy,
        max_new_tokens=max_new_tokens,
        output_scores=True,
        return_dict_in_generate=True
    )
    noisy_logits = torch.stack(out_noisy.scores, dim=1)

    
    beta = 0.1
    plausibilityTensor = clean_logits_asd > beta

    adjustedClean = torch.where(plausibilityTensor, clean_logits_asd, torch.tensor(0.0, device=noisy_logits.device))
    adjustedNoisy = torch.where(plausibilityTensor, noisy_logits, torch.tensor(0.0, device=noisy_logits.device))

    print("beta finished")

    alpha = 1.0
    ASD_VCD_logits = (1 + alpha) * adjustedClean - alpha * adjustedNoisy

    input_len = inputsClean["input_ids"].shape[-1]  # Number of prompt tokens
    generated_logits = ASD_VCD_logits[input_len : input_len + 64]

    asd_vcd_processor = ASD_VCDProcessor(generated_logits)
    generated_ids = model.generate(
        input_ids=inputsClean["input_ids"],
        attention_mask=inputsClean["attention_mask"],
        pixel_values=inputsClean["pixel_values"],
        logits_processor=[asd_vcd_processor],
        max_new_tokens=max_new_tokens,
        do_sample=False,
    )
    
    caption = processor.batch_decode(generated_ids, skip_special_tokens=True)[0]
    descSaver(caption, filename)
    return caption


